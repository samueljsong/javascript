/**
 * Arrow functions
 *
 * concise synax for creating functions.
 */

let sum = (a, b) => a + b;

/**
 * if we only have one argument, then we don't have to use the parenthesis
 */

/**
 * Sometimes we need more complex functions, then we can use curly brances
 * to make multiline arrow functions
 *
 * BUT when we use curly braces, we must specify what we are returning.
 */

let multiplication = (a, b) => {
    if (a && b) {
        return a * b;
    }
};
